cars = ['bmw', 'audi', 'toyota', 'subaru']

# sort a list permanently and alphabetically
cars.sort()
print(cars)
# sort a list in reverse order
cars.sort(reverse=True)
print(cars)

# sort a list temporarily
print(sorted(cars))
print(cars)

# print a list in reverse order
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(cars)
cars.reverse()
print(cars)

# find the length of a list
length = len(cars)
print(length)